namespace CountingBits
{
    internal class Program
    {
        static void Main(string[] args)
        {
            CountBits(5);
        }

        // [0,1,1,2,1,2]

        /*
         

        | `i` | Binary | `i >> 1` | Binary(`i >> 1`) | `i & 1` | `ans[i]` (count of 1's) |
        | --- | ------ | -------- | ---------------- | ------- | ----------------------- |
        | 0   | 0000   | -        | -                | 0       | 0                       |
        | 1   | 0001   | 0        | 0000             | 1       | 0 + 1 = 1               |
        | 2   | 0010   | 1        | 0001             | 0       | 1 + 0 = 1               |
        | 3   | 0011   | 1        | 0001             | 1       | 1 + 1 = 2               |
        | 4   | 0100   | 2        | 0010             | 0       | 1 + 0 = 1               |
        | 5   | 0101   | 2        | 0010             | 1       | 1 + 1 = 2               |
        | 6   | 0110   | 3        | 0011             | 0       | 2 + 0 = 2               |
        | 7   | 0111   | 3        | 0011             | 1       | 2 + 1 = 3               |
        | 8   | 1000   | 4        | 0100             | 0       | 1 + 0 = 1               |
        | 9   | 1001   | 4        | 0100             | 1       | 1 + 1 = 2               |
        | 10  | 1010   | 5        | 0101             | 0       | 2 + 0 = 2               |


          00000001

          00000000 0
          00000001 1
          00000010 2
          00000011 3
          00000100 4
          00000101 5
          00000110 6
          00000111 7
          00001000 8

          

         */
        public static int[] CountBits(int n)
        {
            int[] ans = new int[n + 1];

            for (int i = 1; i <= n; i++)
            {
                ans[i] = ans[i >> 1] + (i & 1);
            }

            return ans;
        }


    }
}
